// data.js
// 1번부터 50번까지의 카드 내용을 여기에 작성합니다.
const cardData = {
    1: "첫 번째 카드 내용입니다! 🎁",
    2: "두 번째 카드: 꽝! 💣",
    3: "세 번째 카드: 커피 쿠폰 당첨 ☕",
    4: "네 번째 카드 내용입니다.",
    5: "다섯 번째 카드 내용입니다.",
    // ... 이런 식으로 50번까지 자유롭게 작성하세요. 
    // 작성하지 않은 번호는 자동으로 "기본 메시지"가 출력됩니다.
};

// 50번까지 빈 데이터 기본 세팅 (위에서 직접 입력하지 않은 번호를 채워줍니다)
for (let i = 1; i <= 50; i++) {
    if (!cardData[i]) {
        cardData[i] = `아직 내용을 입력하지 않은 ${i}번 카드입니다.`;
    }
}
